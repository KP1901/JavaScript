// normal function with parameter

// Example 1: Greeting with a Parameter
function greet(name) {
  console.log("hi " + name);
}
greet("kiran");

// Example 2: Adding Two Numbers

function addNumbers(a, b) {
  console.log(a + b);
}
addNumbers(10, 20);

// Example 3: Concatenating Strings

function fullName(firstName, lastName) {
  console.log(firstName + " " + lastName);
}

fullName("Kiran", "Patodekar");

/*
✅ Key Points:
-Parameters are placeholders inside the function.
-Arguments are actual values you pass.
-Functions can perform tasks using these values without returning anything.
-You can have multiple parameters separated by commas.
*/
