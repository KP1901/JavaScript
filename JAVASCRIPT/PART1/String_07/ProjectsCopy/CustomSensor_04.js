  text = text.trim();

  let arr = text.split(/\s+/g);

  let res1 = arr.map((wr) => {
    let cleanWord = wr.replace(/[^a-zA-Z0-9]/g, "").toLowerCase();

    if (!badWords.includes(cleanWord)) {
      return wr;
    }

    return wr.replace(/[a-z]/gi, "*");
  });
  return res1.join(" ");
