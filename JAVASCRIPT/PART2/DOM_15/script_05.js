/*
5️⃣ Creating and Removing Elements

-Until now we only selected and modified existing elements.
-Now we will learn how to create new elements dynamically.
-This is how things like:

Todo apps
Shopping carts
Notifications
Chat messages

are built.

Create element
     ↓
Add content
     ↓
Insert into DOM

1️⃣ createElement() => create new Dom element

document.createElement("div")

2️⃣ append()

Adds the element to a parent.

3️⃣ prepend()

Adds element at the beginning.

list.prepend(li);

Example result:

<ul>
  <li>Apple</li>
  <li>Banana</li>
</ul>

4️⃣ remove()

Removes an element.

Example :

li.remove();
*/
